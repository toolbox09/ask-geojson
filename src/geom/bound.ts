


export class Bound {
    
    public minX : number;
    public maxX : number;
    public minY : number;
    public maxY : number;

    public constructor(minX : number, maxX : number, minY : number, maxY : number) {
        this.minX = minX;
        this.maxX = maxX;
        this.minY = minY;
        this.maxY = maxY;
    }
}